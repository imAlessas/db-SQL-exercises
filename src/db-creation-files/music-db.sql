DROP DATABASE IF EXISTS music;

CREATE DATABASE music; /* Creation of the database */

\c music

CREATE TABLE PEOPLE (
    PersonName VARCHAR(20) PRIMARY KEY,
    BirthYear INTEGER NOT NULL
);

CREATE TABLE ALBUMS (
    AlbumId INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    AlbumTitle VARCHAR(50) NOT NULL,
    AlbumYear INTEGER NOT NULL
);

CREATE TABLE SONGS (
    SongId INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    SongTitle VARCHAR(50) NOT NULL,
    SongYear INTEGER
);

CREATE TABLE ALBUM_SONGS (
    AlbumId INTEGER REFERENCES ALBUMS(AlbumId) ON DELETE CASCADE ON UPDATE CASCADE,
    TrackId INTEGER,
    SongId INTEGER NOT NULL REFERENCES SONGS(SongId) ON DELETE CASCADE ON UPDATE CASCADE,
    PRIMARY KEY(AlbumId, TrackId),
    UNIQUE(AlbumId, SongId)
);

CREATE TABLE SONG_AUTHORS (
    PersonName VARCHAR(20) REFERENCES PEOPLE(PersonName) ON DELETE CASCADE ON UPDATE CASCADE,
    SongId INTEGER REFERENCES SONGS(SongId) ON DELETE CASCADE ON UPDATE CASCADE,
    PRIMARY KEY(PersonName, SongId)
);

CREATE TABLE SONG_SINGERS (
    PersonName VARCHAR(20) REFERENCES PEOPLE(PersonName) ON DELETE CASCADE ON UPDATE CASCADE,
    SongId INTEGER REFERENCES SONGS(SongId) ON DELETE CASCADE ON UPDATE CASCADE,
    PRIMARY KEY(PersonName, SongId)
);


